package com.jgg.lessons.p0110;

public class CharAssignment {

  public static void main(String[] args){

    // Assign 'j' to a char and print
    char charWithSingleQuotes = 'j';
    System.out.println(charWithSingleQuotes);

    // Assign the unicode of "check" emoji to a char and print
    char checkEmoji = '\u2705';
    System.out.println(checkEmoji);

    // Assign 85 to a char and print
    char charFromAsciiCode = 85;
    System.out.println(charFromAsciiCode);
  }

}




