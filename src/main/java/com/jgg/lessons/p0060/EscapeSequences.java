package com.jgg.lessons.p0060;

public class EscapeSequences {

  public static void main(String[] args){

    // Print "Hello" and "World" in separate lines by using single println function
    System.out.println("Hello\nWorld!");

    // Print "Name: John  Doe" with two tabs between each by using single println function
    System.out.println("Name:\t\tJohn\t\tDoe");

    // Print <He said, "Hello!"> by using single println function
    System.out.println("He said, \"Hello!\"");

    // Print Omega (Î©) character
    System.out.println("\u03A9"); // Greek capital letter Omega
  }
}




