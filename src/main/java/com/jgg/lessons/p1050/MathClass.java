package com.jgg.lessons.p1050;

public class MathClass {

  public static void main(String[] args){

    // Calculate 2 raised to the power of 3
    System.out.println(Math.pow(2, 3)); // 8.0

    // Calculate the square root of 16
    System.out.println(Math.sqrt(16)); // 4.0

    // Print PI
    System.out.println(Math.PI); // 3.141592653589793

    // Calculate the closest integer to 4.3
    System.out.println(Math.round(4.3)); // 4

    // Round 9.9 downward to the nearest number
    System.out.println(Math.floor(9.9)); // 9.0

    // Round 9.1 upward to the nearest number
    System.out.println(Math.ceil(9.9)); // 10.0

    // Get the absolute value of -3
    System.out.println(Math.abs(-3)); // 3

    // Get the max of 3 and 5
    System.out.println(Math.max(3,5)); // 5

  }
}
