package com.jgg.lessons.p0200;

public class AssignmentOperators {

  public static void main(String[] args){

    // Assign 10 to int variable
    int x = 10; // Assigns the value 10 to the variable x

    // Assign 5 to int variable and add up 3
    int a = 5;
    a += 3; // Equivalent to a = a + 3; // a is assigned the value 8

    // Assign 10 to int variable and reassign minus 4 of it
    int y = 10;
    y -= 4; // Equivalent to y = y - 4; // y is assigned the value 6

    // Assign 2 to int variable and reassign it multiplying with 3
    int z = 2;
    z *= 3; // Equivalent to z = z * 3; // z is assigned the value 6

    // Assign 10 to int variable and reassign it dividing by 2
    int b = 10;
    b /= 2; // Equivalent to b = b / 2; // b is assigned the value 5

    // Assign 11 to int variable and reassign it the reminder of division by 3
    int c = 11;
    c %= 3; // Equivalent to c = c % 3; // c is assigned the value 2

  }

}
