package com.jgg.lessons.p0090;

public class PrintingVariables {

  public static void main(String[] args){

    // Initialize an int variable and print
    int age = 20;
    System.out.println(age);

    // Initialize a String variable and print
    String name = "Jane";
    System.out.println(name);

    // Assign "20 * 5" to an int variable and print
    int multiplication = 20 * 5;
    System.out.println(multiplication);

    // Assign "Hello" + " " + "World" to a String variable and print
    String helloWorld = "Hello" + " " + "World";
    System.out.println(helloWorld);

    // Assign A to a char variable and print
    char aUpperCase = 'A';
    System.out.println(aUpperCase);

    // Assign 106 to a char variable and print
    char whatIsThisChar = 106;
    System.out.println(whatIsThisChar);
  }
}




