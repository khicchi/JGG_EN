package com.jgg.lessons.p0230;

public class RelationalOperatorsGreaterLesser {

  public static void main(String[] args){

    // Compare if 11 is greater than 10
    System.out.println(11 > 10); // true

    // Compare if 11 is greater than or equal to 10
    System.out.println(11 >= 10); // true

    // Compare if 10 is greater than or equal to 10
    System.out.println(10 >= 10); // true

    // Compare if 10 is smaller than 10
    System.out.println(10 < 10); // false

    // Compare if 'C' is smaller than 'D'
    System.out.println('C' < 'D'); // true

    // Compare if 'A' is smaller than or equal to 'A'
    System.out.println('A' <= 'A'); // true

    // Compare if "A" is smaller than "B" as strings
    //System.out.println("A" < "B"); // not doable

  }
}
