package com.jgg.homework.p0040;

public class ConcatenationSolution {

  public static void main(String[] args){

    // TASK-1: Concatenate <Don’t count your chickens > with <before they hatch>
    //         using the + operator and print with the println function
    System.out.println("Don’t count your chickens " + "before they hatch");

    System.out.println("************************** TASK 1 FINISHED **************************");

    // TASK-2: Print your full name by concatenating your first name and last name
    System.out.println("Jane " + "Doe");

    System.out.println("************************** TASK 2 FINISHED **************************");

    // TASK-3: Print <Don’t judge a book by its cover> by concatenating each word
    System.out.println("Don't " + "judge " + "a " + "book " + "by " + "its cover");

    System.out.println("************************** TASK 3 FINISHED **************************");

    // TASK-4: Print <Don't procrastinate> by concatenating each word
    System.out.println("Don't " + "procrastinate");

    System.out.println("************************** TASK 4 FINISHED **************************");
  }

}
